/* A partir do array de frutas basket, retorne um objeto que contenha o nome da fruta como chave e a quantidade de vezes que ela aparece no array como valor. Por exemplo, o array [‘Melancia’, ‘Abacate’, ‘Melancia’, ‘Melancia’, ‘Uva’], deverá retornar { Melancia: 3, Abacate: 1, Uva: 1 } quando passado como argumento para a função. */

let fruits = {};
for(let index = 0; index < basket.length; index = index +1){
  let soma = 0;
  for(cont = 0; cont < basket.length; cont = cont + 1){
    if( basket[index] === basket[cont]){
      soma = soma + 1
    }
  }
  fruits[basket[index]] = soma;
}
let termosDaMensagem =[];
for(let index in fruits){
  let mensagem = `${fruits[index]} ${index}`
  if(fruits[index] > 1){
    mensagem = mensagem + 's';
  }
  termosDaMensagem.push(mensagem)
}
console.log(`Sua cesta possui: ${termosDaMensagem.join(",")}.`)