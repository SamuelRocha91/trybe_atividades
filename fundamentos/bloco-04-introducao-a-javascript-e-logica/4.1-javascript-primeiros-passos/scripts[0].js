    // Faça cinco programas, um para cada operação aritmética básica. Seu programa deve ter duas constantes, a e b, definidas no começo com os valores que serão operados. 
    
    const a = 10;
    const b = 5;

    // Soma

    let soma = a + b;
    console.log(soma)

    // Subtração

    let subtracao = a - b;
    console.log(subtracao)

    // Divisão

    let divisao = a / b;
    console.log(divisao)

    // Multiplicação

    let multiplicacao = a * b;
    console.log(multiplicacao)

    // Exponenciação

    let exponenciacao = a ** b;
    console.log(exponenciacao)

    
